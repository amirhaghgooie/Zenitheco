<p class="MsoNormal">If your ducts look dirty, you should definitely consider duct cleaning.<o:p></o:p></p>

<p class="MsoNormal">Your&nbsp;<a href="http://zenitheco.ca/furnace-repair-ottawa.html">furnace</a>, ventilation, and&nbsp;<a href="http://zenitheco.ca/aircon.html">air conditioning</a> systems all use ducts to transfer air. The ducts can become filled with dust, mold, and allergens, especially if you have done any home improvement, you have just moved into your home, or you notice any mould or water in your ducts. Your HVAC system is responsible for air circulation, and a build-up of contaminants in your ducts means the system must work harder. This extra pressure can shorten the life of your system. These pollutants not only create an unhealthy home environment but also reduce your&nbsp;<a href="http://zenitheco.ca/blog/expert-hvac-advice/">HVAC</a>&nbsp;system&rsquo;s efficiency.<o:p></o:p></p>

